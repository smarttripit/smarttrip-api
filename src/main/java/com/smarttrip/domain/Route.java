package com.smarttrip.domain;

public class Route {
    private String routeId;

    private String name;

    private String feature;

    private Short period;

    private String map;

    private String briefRecommendation;

    private Integer commentCount;

    private Float commetRatio;

    private Integer purchaseCount;

    private String regionId;

    private String detail;

    private String feeDescription;

    private String bookingNotice;

    private String detailRecommondation;

    public String getRouteId() {
        return routeId;
    }

    public void setRouteId(String routeId) {
        this.routeId = routeId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFeature() {
        return feature;
    }

    public void setFeature(String feature) {
        this.feature = feature;
    }

    public Short getPeriod() {
        return period;
    }

    public void setPeriod(Short period) {
        this.period = period;
    }

    public String getMap() {
        return map;
    }

    public void setMap(String map) {
        this.map = map;
    }

    public String getBriefRecommendation() {
        return briefRecommendation;
    }

    public void setBriefRecommendation(String briefRecommendation) {
        this.briefRecommendation = briefRecommendation;
    }

    public Integer getCommentCount() {
        return commentCount;
    }

    public void setCommentCount(Integer commentCount) {
        this.commentCount = commentCount;
    }

    public Float getCommetRatio() {
        return commetRatio;
    }

    public void setCommetRatio(Float commetRatio) {
        this.commetRatio = commetRatio;
    }

    public Integer getPurchaseCount() {
        return purchaseCount;
    }

    public void setPurchaseCount(Integer purchaseCount) {
        this.purchaseCount = purchaseCount;
    }

    public String getRegionId() {
        return regionId;
    }

    public void setRegionId(String regionId) {
        this.regionId = regionId;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getFeeDescription() {
        return feeDescription;
    }

    public void setFeeDescription(String feeDescription) {
        this.feeDescription = feeDescription;
    }

    public String getBookingNotice() {
        return bookingNotice;
    }

    public void setBookingNotice(String bookingNotice) {
        this.bookingNotice = bookingNotice;
    }

    public String getDetailRecommondation() {
        return detailRecommondation;
    }

    public void setDetailRecommondation(String detailRecommondation) {
        this.detailRecommondation = detailRecommondation;
    }
}